const char* AI_GetNodeString(const AI_tdstNodeInterpret* node) {
  switch (node->eType) {
    case AI_E_ti_KeyWord:     return AI_GetKeyWordString(node->uParam.ulValue);     break;
    case AI_E_ti_Procedure:   return AI_GetProcedureString(node->uParam.ulValue);   break;
    case AI_E_ti_Function:    return AI_GetFunctionString(node->uParam.ulValue);    break;
    case AI_E_ti_Condition:   return AI_GetConditionString(node->uParam.ulValue);   break;
    case AI_E_ti_Operator:    return AI_GetOperatorString(node->uParam.ulValue);    break;
    case AI_E_ti_Field:       return AI_GetFieldString(node->uParam.ulValue);       break;
    case AI_E_ti_MetaAction:  return AI_GetMetaActionString(node->uParam.ulValue);  break;
    default: return AI_GetTypeInterpretString(node->eType);
  }
}

